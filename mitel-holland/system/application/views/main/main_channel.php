<p>Mitel Total Solutions: de oplossing.
Om Mitel PARTNERs te ondersteunen om bovenstaande
punten goed te kunnen adresseren heeft Mitel besloten
het Total Solutions-programma, dat al meer dan 2 jaar
succesvol loopt in het Verenigd Koninkrijk, naar
Nederland te halen.</p>
<p>In samenwerking met gerenommeerde banken in
Nederland heeft Mitel een gloednieuw product
ontwikkeld, waarmee u als Mitel PARTNER in staat bent
om managed service gerelateerde diensten financieel
interessant aan te bieden, zonder dat dit beslag legt op
uw cash flow en kredietfaciliteiten.</p>
<p>Mitel Total Solutions zorgt er ook voor dat u uw relatie
met uw klant effectiever benut over meerdere jaren. Tot
slot: financieringsoplossingen zijn doorgaans complex en
lastig. Mitel heeft de afgelopen maanden veel geld en
tijd ge&iuml;nvesteerd om Mitel Total Solutions van heldere
documenten, handige tools en lokale ondersteuning te
voorzien. Dit om het gebruik van het programma voor u
zo transparant en eenvoudig mogelijk te maken.</p>
<h3>In het kort:</h3>

<ul>
<li>
Mitel Total Solutions geeft u de structuur die nodig
is om managed service overeenkomsten effectief
te gaan inzetten</li>

<li>Financiering wordt verzorgd in samenwerking met
gerenommeerde (Nederlandse) banken</li>

<li>Geen aanspraak op uw cash flow
en kredietfaciliteiten</li>

<li>Een helder proces en goede ondersteuning
Mitel Total Solutions is gebaseerd op:</li>

<li>E&eacute;n contract tussen de Mitel PARTNER en de klant,
geen aanvullende documenten nodig</li>

<li>E&eacute;n contract dat praktisch alle elementen uit uw
aanbod kan bevatten</li>

<li>Communicatie hard- en software</li>
<li>Netwerkapparatuur</li>
<li>Installatie en eenmalige diensten</li>
<li>(Mobiele) netwerkdiensten en tikken, e.d.</li>

</ul>

<h3>Meer effect over meerdere jaren:
Technology Refresh</h3>
<p>
Total Solutions biedt u een interessant tool om over
meerdere jaren een intensievere, en daarmee doorgaans
commercieel interessantere relatie met uw klanten op te
bouwen. Zo biedt het programma u inzicht in de kansen
om na een bepaalde tijd de klant 'kosteloos' een
zogenaamde 'Technology Refresh' of uitbreiding aan te
bieden, indien de klant het contract met u voor een
bepaalde tijd verlengt. Goed nieuws voor uw klant. Goed
nieuws voor u!</p>
